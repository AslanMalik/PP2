# Check True or False
print(bool("abc"))  # True
print(bool(123))    # True
print(bool(["apple", "cherry", "banana"]))  # True
print(bool(False))  # False
print(bool(None))   # False
print(bool(0))      # False
print(bool(""))     # False
print(bool(()))     # False
print(bool([]))     # False
print(bool({}))     # False


